const MAX_WEAPON_LEVEL = 90;

if (
  WEAPON_LEVEL_EXPERIENCE_COSTS.length !== MAX_WEAPON_LEVEL ||
  WEAPON_LEVEL_T_CREDIT_COSTS.length !== MAX_WEAPON_LEVEL
) {
  throw new Error("Weapon level cost tables must contain exactly 90 entries.");
}

export function getWeaponLevelCosts(level: number) {
  const index = level - 1;

  if (index < 0 || index >= MAX_WEAPON_LEVEL) {
    return {};
  }

  return {
    experienceCost: WEAPON_LEVEL_EXPERIENCE_COSTS[index],
    tCreditCost: WEAPON_LEVEL_T_CREDIT_COSTS[index],
  };
}
